// installing npm pakages 
// npm install <package name> or npm i <package name>
// with version npm install <packagename>@<version> or npm install <packagename>@<version range>

// installing latest npm pakages
// npm install <name>@latest

// npm install --save <name> (Install dependencies and add to pakage.json file as dependency)
// npm install -S <name> (shortcut version --save) or
// npm i -S <name>

// npm install --save-dev <name> => Install development dependencies and adds to package.json as dev dependency
// short command => npm install -D <name>

// to see list of installed packages => npm list
// to install all npm dependencies use => npm install or npm i

// uninstalling packages
// npm uninstall <package name> or npm r <package name>
// npm uninstall --Save <package name> or npm uninstall -s <package name> 
// will uninstall the package as well as removes it from package.json list of dependencies.

// Node.js package configurations are contained in a file called package.json we can create it by 
// npm init or npm init -y(for default values)

